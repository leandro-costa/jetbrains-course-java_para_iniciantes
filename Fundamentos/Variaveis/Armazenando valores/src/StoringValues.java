public class StoringValues
{
   public static void main(String[] args)
   {
      int hoursInADay = 24;
      double pi = 3.1416;
      boolean isTheSkyBlue;
      String languageName;

      isTheSkyBlue = true;
      languageName = "Java";

      System.out.println("Horas em um dia: " + hoursInADay);
      System.out.println("Pi está perto de " + pi);
      System.out.println("O céu é azul?: " + isTheSkyBlue);
      System.out.print("A linguagem de programação que estamos aprendendo é " + languageName);
   }
}